     private void validParameters(boolean error){
        if(error){
            if(jTextField2.getText().equals(""))
               jTextField2.setBackground(Color.red);
            
        } else{
            jTextField2.setBackground(Color.WHITE);
         
        } 
   }
      private void showBook(String loanId){
          DefaultComboBoxModel<Book> book = new DefaultComboBoxModel<>();
          
          for(Loan l : library.getLoans())
              if(l.getLoanId().equalsIgnoreCase(loanId) && !l.isIsReturned())
                  book.addElement(l.getBook());
          jComboBox1.setModel(book);
      }
      
      private Result returnBook(){
          Book book = (Book) jComboBox1.getModel().getSelectedItem();
          return library.returnBook(book);
          
      }
