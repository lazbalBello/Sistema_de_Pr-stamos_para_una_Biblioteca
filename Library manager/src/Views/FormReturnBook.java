    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField2.getText().trim().isEmpty()){
            validParameters(true);
            JOptionPane.showMessageDialog(rootPane, "Enter loan ID");
        }else{
            String loanId = jTextField2.getText();
            showBook(loanId);
            if(jComboBox1.getModel().getSize()== 0)
                JOptionPane.showMessageDialog(rootPane, "Wrong loan id");
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jComboBox1.getModel().getSize()==0 && !jTextField2.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "There are no books for that id"); 
            validParameters(true);
            
        }else{
            showBook(jTextField2.getText());
            Result r = returnBook();
             JOptionPane.showMessageDialog(rootPane,r.getMessage() ); 
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

